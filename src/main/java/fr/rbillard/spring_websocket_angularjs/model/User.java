package fr.rbillard.spring_websocket_angularjs.model;


public class User {

	
	private long id;
	private String login;

	
	public long getId() {
		return id;
	}
	public void setId( long id ) {
		this.id = id;
	}

	
	public String getLogin() {
		return login;
	}
	public void setLogin( String login ) {
		this.login = login;
	}
	

}
